#include <unistd.h>

int	main(void)
{
	char c;
	char tmp;

	c = 'a';
	while (c <= 'z')
	{
		if (c % 2 == 0)
			tmp = c - 32;
		else
			tmp = c;
		write(1, &tmp, 1);
		c++;
	}
	c = '\n';
	write(1, &c, 1);
	return (0);
}
